package org.example.enums;

// Enum to represent the genre of a movie in the booking system.
public enum MovieGenre {
  ACTION, // High-energy films with physical stunts and battles
  COMEDY, // Light-hearted films designed to entertain and amuse
  DRAMA, // Character-driven films focused on emotional narratives
  HORROR, // Films designed to frighten and create suspense
  ROMANCE, // Films centered around love and relationships
  THRILLER, // Suspenseful films with high tension and plot twists
  SCI_FI, // Science fiction films set in futuristic or alternate worlds
  ANIMATION, // Animated films for all age groups
  DOCUMENTARY, // Non-fiction films presenting real-world events or topics
  FANTASY; // Films involving magical or supernatural elements
}
